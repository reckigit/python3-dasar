'''
Tanda plus (+) adalah operator penggabungan string 
dan tanda bintang (*) adalah operator pengulangan.
'''
myname = 'Recki Frandes'
print(myname)           # CETAK STRING myname DI LAYAR
print(myname[0])        # CETAK KARAKTER STRING PERTAMA myname DI LAYAR
print(myname[6:10])     # CETAK KARAKTER STRING myname MULAI DARI KE-7 SAMPAI DENGAN KE-13 DI LAYAR
print(myname[6:])       # CETAK KARAKTER STRING myname MULAI DARI KE-7 DI LAYAR
print(myname *2)        # CETAK STRING myname 2x DI LAYAR
print(myname + 'TEST')  # CETAK STRING myname BERSAMBUNG STRING DI LAYAR
