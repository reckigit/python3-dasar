'''
Tanda plus (+) adalah operator rangkaian daftar, 
dan tanda bintang (*) adalah operator pengulangan.
'''
myjobs = ['sopir','dagang','coding']
myhobbies = ['baca','tulis','nonton']
print(myjobs)                   # CETAK LENGKAP LIST myjobs DI LAYAR
print(myhobbies)                # CETAK LIST myhobbies DI LAYAR
print(myjobs[0])                # CETAK ELEMEN LIST PERTAMA DARI LIST myjobs DI LAYAR
print(myjobs[1:2])              # CETAK ELEMEN LIST MULAI DARI KE-2 SAMPAI KE-2 DI LAYAR
print(myjobs[1:3])              # CETAK ELEMEN LIST MULAI DARI KE-2 SAMPAI KE-3 DI LAYAR
print(myjobs[0:])               # CETAK ELEMEN LIST MULAI DARI PERTAMA DI LAYAR
print(myhobbies *2)             # CETAK LIST myhobbies 2x DI LAYAR
print(myjobs + myhobbies)       # CETAK LIST myhobbies DITAMBAH myhobbies DI LAYAR
